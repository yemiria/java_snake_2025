package block;


import java.util.Arrays;

public class Map {


    public static int[][] GameMap()
    {
    //------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //地图
        //wd为width,ht为height
        int wd = 50;// 地图宽度
        int ht = 9; // 地图高度
        int[][] map = new int[ht][wd];
    //--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        // 打印地图
        printMap(map);
        //地图内容
        wallMap(map);
        //苹果设置
        appleMap(map);
        //蛇头设置
        HeadMap(map);
        //蛇身设置
        BodyMap(map);
    //------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

        return map;
    }

    public static void printMap(int[][] map)
    {
    //------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        // 设置地图
        wallMap(map);
        //苹果设置
        appleMap(map);
        //蛇头设置
        HeadMap(map);
        //蛇身设置
        BodyMap(map);
    //--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //打印地图
        //hg表示地图的横着的墙
        String hg = "-";
        //sg表示地图的竖着的墙
        String sg = "|";
        String ap = "A";
        //bh表示蛇头(BodyHead)
        String bh = "H";
        //bb表示蛇身体(BodyBody)
        String bb = "B";
    //--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //用循环来作地图
        for (int i = 0; i < map.length; i++)
        {
            for (int j = 0; j < map[i].length; j++)
            {
                if (map[i][j] == 0)
                {
                    System.out.print(" ");
                }
                else if (map[i][j] == 1)
                {
                    System.out.print(hg);
                }
                else if (map[i][j] == 2)
                {
                    System.out.print(sg);
                }
                else if (map[i][j] == 3)
                {
                    System.out.print(ap);
                }
                else if (map[i][j] == 4)
                {
                    System.out.print(bh);
                }
                else if (map[i][j] == 5)
                {
                    System.out.print(bb);
                }

            }
            System.out.println();

        }

    }

    public static void wallMap(int[][] map)
    {
        for (int i = 0; i < map.length; i++)
        {
            for (int j = 0; j < map[i].length; j++)
            {
                if (i == 0 || i == map.length - 1)
                {
                    map[i][j] = 1;
                }
                else if (j == 0 || j == map[i].length - 1)
                {
                    map[i][j] = 2;
                }
            }
        }
    }

    public static void appleMap(int[][] map)
    {
        for (int i = 0; i < map.length; i++)
        {
            for (int j = 0; j < map[i].length; j++)
            {
                //想用随机数来设置苹果的位置，但是失败了，最后就这样了先
                // if (i==Math.random()* map.length||j==Math.random()* map[i].length) {
                    //map[i][j] = 3;
                if (i==map.length/4&j==map[i].length/4)
                {
                    map[i][j] = 3;
                }


            }
        }
    }
    public static void HeadMap(int[][] map)
    {
        for (int i = 0; i < map.length; i++)
        {
            for (int j = 0; j < map[i].length; j++)
            {
                if (i == map.length / 2 & j == map[i].length / 2)
                {
                    map[i][j] = 4;
                }
            }
        }
    }
    public static void BodyMap(int[][] map)
    {
     for (int i = 0; i < map.length; i++)
         {
             for (int j = 0; j < map[i].length; j++)
             {
                 if (i == map.length / 2 & j == map[i].length / 2+1)
                 {
                     map[i][j] = 5;
                     map[i][j+1]=map[i][j];

                 }
             }
         }
    }

}
